class Assignment_OP{
	public static void main(String [] args){
	int num1=10,num2=20;
	
	
	num1+=num2;//here perform operation num1=num1+num2
	System.out.println("The sum Assignment operator:" +num1);
	
	num1-=num2;//here perform operation num1=num1-num2
	System.out.println("The Subtract Assignment operator:"+num1);
	
	num1*=num2;//here perform operation num1=num1*num2
	System.out.println("The Multiply Assignment operator:"+num1);
	
	num1/=num2;//here perform operation num1=num1/num2
	System.out.println("The Division Assignment operator:"+num1);
	
	num1%=num2;//here perform operation num1=num1%num2
	System.out.println("The Modulus Assignment operator:"+num1);
	}
}
